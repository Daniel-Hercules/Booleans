using System;

namespace Booleans
{
    class Program
    {
        static void Main(string[] args)
        {
            // Declaration of Boolean variables 
            Boolean isCSharpFun = true;
            Boolean isTheWorldGoingToEnd = false;

            // Typecasting the value of the variables
            Console.WriteLine(isCSharpFun);
            Console.WriteLine(isTheWorldGoingToEnd);

            // Printing out result of boolean arguement
            
            int x = 10;
            int y = 10;

            // Prints out if x is bigger than y
            Console.WriteLine(x > y);

            // Prints out if x and y are equal
            Console.WriteLine(x == y);


        }
    }
}
